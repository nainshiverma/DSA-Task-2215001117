import java.util.*;
public class dddd{
    public static int multiplication(int a , int b){
        int c = a*b;
        return c;
    }
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int x = sc.nextInt();
        int y = sc.nextInt();
        int z  = multiplication(x , y);
        System.out.println(z);

    }
    
    
}